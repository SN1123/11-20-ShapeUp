// A Rectangle object represents a rectangle defined by an origin point (bottom left corner), plus width and height.
public class Rectangle {
  int Origin;
  int Width;
  int Highth;
  
  public Rectangle (int initOrigin){
    Origin = initOrigin;
  }
  
  public Rectangle (int initWidth, int initHighth){
    Width = initWidth;
    Highth = initHighth;
  }
}
